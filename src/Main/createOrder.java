    private void jLabel140MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel140MouseClicked

        ArrayList<String> name = new ArrayList();
        ArrayList<String> type = new ArrayList();
        ArrayList<Integer> price = new ArrayList();
        ArrayList<Integer> quantity = new ArrayList();
        for (int i = 0; i < tblcart.getRowCount(); i++) {
            name.add(String.valueOf(tblcart.getValueAt(i, 0)));
            type.add(String.valueOf(tblcart.getValueAt(i, 1)));
            quantity.add(Integer.parseInt(""+tblcart.getValueAt(i, 2)));
            price.add(Integer.parseInt(""+tblcart.getValueAt(i, 3)));
        }
        int selected = tblproducts.getSelectedRow();
        String selectname = String.valueOf(tblproducts.getValueAt(selected, 0));
        String selecttype = String.valueOf(tblproducts.getValueAt(selected, 1));
        int selectprice = Integer.parseInt(""+tblproducts.getValueAt(selected, 2));
        if(name.indexOf(selectname) != -1){
            quantity.set(name.indexOf(selectname), (quantity.get(name.indexOf(selectname)))+1);
            price.set(name.indexOf(selectname), (Products.priceList.get(Products.nameList.indexOf(selectname)))*quantity.get(name.indexOf(selectname)));
            }else{
            name.add(selectname);
            type.add(selecttype);
            price.add(selectprice);
            quantity.add(1);
        
        }
        DefaultTableModel model = (DefaultTableModel) tblcart.getModel();
        model.setRowCount(0);
        Object rowData[] = new Object[4];
        for (int i = 0; i < name.size(); i++) {
            rowData[0] = name.get(i);
            rowData[1] = type.get(i);
            rowData[2] = quantity.get(i);
            rowData[3] = price.get(i);
            model.addRow(rowData);
        }
        int total = 0;
        for (int i = 0; i < price.size(); i++) {
            total += price.get(i);
        }
        txttotal.setText("" + total);
    }//GEN-LAST:event_jLabel140MouseClicked

    private void jLabel141MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel141MouseClicked
        ArrayList<String> name = new ArrayList();
        ArrayList<String> type = new ArrayList();
        ArrayList<Integer> price = new ArrayList();
        ArrayList<Integer> quantity = new ArrayList();
        for (int i = 0; i < tblcart.getRowCount(); i++) {
            name.add(String.valueOf(tblcart.getValueAt(i, 0)));
            type.add(String.valueOf(tblcart.getValueAt(i, 1)));
            quantity.add(Integer.parseInt(""+tblcart.getValueAt(i, 2)));
            price.add(Integer.parseInt(""+tblcart.getValueAt(i, 3)));
        }
        int selected = tblcart.getSelectedRow();
        String selectname = String.valueOf(tblcart.getValueAt(selected, 0));
        int selectquantity = Integer.parseInt(""+tblcart.getValueAt(selected, 2));
        if (selectquantity > 1) {
            quantity.set(name.indexOf(selectname), (quantity.get(name.indexOf(selectname)))-1);
            price.set(name.indexOf(selectname), (Products.priceList.get(Products.nameList.indexOf(selectname)))*quantity.get(name.indexOf(selectname)));
        }else{
            try{
                int rem = 0;
                rem = name.indexOf(selectname);
                name.remove(rem);
                type.remove(rem);
                quantity.remove(rem);
                price.remove(rem);
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        DefaultTableModel model = (DefaultTableModel) tblcart.getModel();
        model.setRowCount(0);
        Object rowData[] = new Object[4];
        for (int i = 0; i < name.size(); i++) {
            rowData[0] = name.get(i);
            rowData[1] = type.get(i);
            rowData[2] = quantity.get(i);
            rowData[3] = price.get(i);
            model.addRow(rowData);
        }
        int total = 0;
        for (int i = 0; i < price.size(); i++) {
            total += price.get(i);
        }
        txttotal.setText("" + total);
    }//GEN-LAST:event_jLabel141MouseClicked

