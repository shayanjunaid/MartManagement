    private void deleteProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteProductActionPerformed
        String search = searchtxt.getText();
        Products p = new Products();
        if(search.equals("")){
            Products.nameList.clear();
            Products.priceList.clear();
            Products.inStockList.clear();
            Products.typeList.clear();
            p.loadProducts();
            DefaultTableModel model = (DefaultTableModel) tblproducts.getModel();
            model.setRowCount(0);
            addProd();
        }else{
            Products.nameList.clear();
            Products.priceList.clear();
            Products.inStockList.clear();
            Products.typeList.clear();
            p.loadNameFilter(search);
            DefaultTableModel model = (DefaultTableModel) tblproducts.getModel();
            model.setRowCount(0);
            addProd();
        }
        
    }//GEN-LAST:event_deleteProductActionPerformed

    private void jLabel138MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel138MouseClicked
        ArrayList<String> name = new ArrayList();
        ArrayList<String> type = new ArrayList();
        ArrayList<Integer> price = new ArrayList();
        ArrayList<Integer> quantity = new ArrayList();
        for (int i = 0; i < tblcart.getRowCount(); i++) {
            name.add(String.valueOf(tblcart.getValueAt(i, 0)));
            type.add(String.valueOf(tblcart.getValueAt(i, 1)));
            quantity.add(Integer.parseInt(""+tblcart.getValueAt(i, 2)));
            price.add(Integer.parseInt(""+tblcart.getValueAt(i, 3)));
        }
        Cart c = new Cart();
        c.setOrderTotal(Integer.parseInt(txttotal.getText()));
        c.setEmployeeName(Employee.loggedInEmp.getEmpName());
        for (int i = 0; i < name.size(); i++) {
            c.addProductNames(name.get(i));
        }
        for (int i = 0; i < type.size(); i++) {
            c.addProductTypes(type.get(i));
        }
        for (int i = 0; i < price.size(); i++) {
            c.addProductPrice(price.get(i));
        }
        for (int i = 0; i < quantity.size(); i++) {
            c.addProductQuantity(quantity.get(i));
        }
        Products p = new Products();
        for (int i = 0; i < name.size(); i++) {
            p.updUnitsSold(name.get(i), quantity.get(i));
            p.updStock(name.get(i), quantity.get(i));
        }
        try{
            Finances f = new Finances();
            f.updateincome(Integer.parseInt(txttotal.getText()));
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        new orderConfirm().setVisible(true);
    }//GEN-LAST:event_jLabel138MouseClicked

